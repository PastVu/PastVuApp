export function getColor(number: number) {
	if (number <= 1834) {
		return '#FA00FF'
	} else if (number <= 1850) {
		return '#EF00FC'
	} else if (number <= 1880) {
		return '#FB00BD'
	} else if (number <= 1895) {
		return '#FD0061'
	} else if (number <= 1930) {
		return '#FF1400'
	} else if (number <= 1950) {
		return '#FF8800'
	} else if (number <= 1970) {
		return '#FFF500'
	} else if (number <= 1980) {
		return '#9FFC00'
	} else if (number <= 2000) {
		return '#1EFF00'
	}
}
